DROP TABLE IF EXISTS nombres_base CASCADE;

CREATE TABLE nombres_base (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre varchar(100) NOT NULL
);

CREATE TABLE apellidos_base (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  apellido varchar(100) NOT NULL
);

CREATE TABLE departamento (
  id_departamento integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo_dane_departamento char(2) NOT NULL UNIQUE,
  nombre_departamento varchar(100) NOT NULL
);

CREATE TABLE municipio (
  id_municipio integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo_dane_municipio char(5) NOT NULL UNIQUE,
  nombre_municipio varchar(100) NOT NULL,
  id_departamento integer NOT NULL REFERENCES departamento (id_departamento)
);

CREATE TABLE tipo_poblacion (
  id_tipo_poblacion integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre_tipo varchar(150)
);

CREATE TABLE beneficiario (
  id_beneficiario integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombres varchar(100),
  documento varchar(20) UNIQUE,
  telefono varchar(20),
  correo varchar(100),
  direccion varchar(150),
  id_municipio integer REFERENCES municipio (id_municipio),
  id_tipo_poblacion integer REFERENCES tipo_poblacion (id_tipo_poblacion)
);

CREATE TABLE cargo (
  id_cargo integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre_cargo varchar(50),
  descripcion varchar(100)
);

CREATE TABLE colaborador (
  id_colaborador integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre_colaborador varchar(100),
  cedula varchar(20) UNIQUE,
  telefono varchar(20),
  correo varchar(100),
  id_cargo integer REFERENCES cargo (id_cargo)
);

CREATE TABLE tipo_ayuda (
  id_tipo_ayuda integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre_ayuda varchar(100),
  descripcion varchar(150)
);

CREATE TABLE entrega_ayuda (
  id_entrega integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  fecha_entrega date,
  id_beneficiario integer REFERENCES beneficiario (id_beneficiario),
  id_municipio_entrega integer REFERENCES municipio (id_municipio),
  id_colaborador integer REFERENCES colaborador (id_colaborador),
  id_tipo_ayuda integer REFERENCES tipo_ayuda (id_tipo_ayuda),
  cantidad integer,
  observaciones text
);

CREATE INDEX idx_municipio_departamento ON municipio (id_departamento);
CREATE INDEX idx_beneficiario_municipio ON beneficiario (id_municipio);
CREATE INDEX idx_beneficiario_tipo_poblacion ON beneficiario (id_tipo_poblacion);
CREATE INDEX idx_colaborador_cargo ON colaborador (id_cargo);
CREATE INDEX idx_entrega_beneficiario ON entrega_ayuda (id_beneficiario);
CREATE INDEX idx_entrega_municipio ON entrega_ayuda (id_municipio_entrega);
CREATE INDEX idx_entrega_colaborador ON entrega_ayuda (id_colaborador);
CREATE INDEX idx_entrega_tipo_ayuda ON entrega_ayuda (id_tipo_ayuda);

